# loop WHILE
x = 1

while x <= 10:
    print(x)
    x += 1

# loop FOR
lista1 = [1, 2, 3, 4, 5]
lista2 = ["ola", "mundo", "!"]
lista3 = [1, "ola", "teste", 9.999, True]

for i in lista3:
    print(i)

# loop FOR c/ RANGE
# range(n1, n2, n3) n1 = num de inicio da lista | n2 = num de fim da lista | n3 = num de intervalo da lista
for i in range(10, 20, 2):
    print(i)
